'''4. Примените все математические операции, например +,-, **, % и т. д., к самой переменной'''

x = 5
x += 4
print('Сложение: ', x)
x -= 2
print('Вычитание: ', x)
x *= 6
print('Умножение: ', x)
x /= 8
print('Простое деление: ', x)
x **= 3
print('Возведение в степень: ', x)
x //= 9
print('Целочисленное деление', x)
x %= 11
print('Остаток от деления', x)